    private void searchLongLat(){
        try{
            double latVal = Double.parseDouble(jTextFieldLatitude.getText());
            double longVal = Double.parseDouble(jTextFieldLongitude.getText());
            //check to see if input coordinates are in a valid range
            if ((latVal>= -90 && latVal<=90)&&(longVal>=-180 && longVal<=180)){
                WeatherInfo.getWeatherForLongAndLat(latVal,longVal);
                //update the text boxes
                jTextFieldLatitude.setText("Latitude: ");
                jTextFieldLongitude.setText("Longitude: ");
                //If weather forecast found for area show main panel
                card.show(jPanelStart, "cardMain");

            }
            else{
                //Shows error a message box
                JOptionPane.showMessageDialog(this,"Please enter valid coordinates","INVALID COORDINATES.",JOptionPane.WARNING_MESSAGE);
            }
        }catch (Exception e ){
            //Shows error a message box
            JOptionPane.showMessageDialog(this,"Please enter valid coordinates","INVALID COORDINATES.",JOptionPane.WARNING_MESSAGE);
        }
    }


