    public GUI() {
        //Create a new list which contains the cities for the combo box
        List<String> cities = new ArrayList<String>();
        try {
            //Read in the cities CSV file and add contents to the cities list
            BufferedReader bufferedReader = new BufferedReader(new FileReader("Cities.csv"));
            String line;
            while ((line = bufferedReader.readLine()) != null) {
                line = line.replace(",", ", ");
                cities.add(line);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        initComponents();
        //Add the contents of the cities list to the combobox
        jComboBoxLocations.setModel(new DefaultComboBoxModel(cities.toArray()));
        this.card = (CardLayout)jPanelStart.getLayout();
    }


